class Dog {
	private int size;
	private String breed;
	private String name;
	
	public void bark() {
		System.out.println("Ruff! Ruff!");
	}
	
	public void setSize(int pSize) {
		size = pSize;
	}
	
	public void setBreed(String pBreed) {
		breed = pBreed;
	}
	
	public void setName(String pName) {
		name = pName;
	}
}